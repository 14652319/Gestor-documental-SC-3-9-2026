print()
print("3. Usuario cierra y vuelve a abrir el visor")
print("   → Lee fecha de localStorage")
print("   → Muestra: '2026-01-02 14:30:15' (verde)")
print()
print("4. Usuario presiona Ctrl+Shift+R (limpiar caché)")
print("   → Se borra localStorage")
print("   → Vuelve a: 'Pendiente'")

print("\n💾 ESTRUCTURA DE LOCALSTORAGE:")
print("-"*80)
print("Key: 'ultimaSincronizacion'")
print("Value: '2026-01-02 14:30:15' (formato YYYY-MM-DD HH:MM:SS)")
print("Scope: Por dominio y navegador")
print("Persistencia: Hasta que el usuario borre datos del navegador")

